'''
Primitive types of variables
'''

# String
my_name = 'Ronald'
print(my_name)

print('My name is: ', my_name)

# Check the data type
print(type(my_name))

# Integer
my_age = 25
print(my_age)

print('My age is: ', my_age)

print(type(my_age))

# Float
my_height = 1.75
print(my_height)

print('My height is: ', my_height)

# Boolean
is_student = True
print(is_student)

print(type(is_student))

is_developer = False
print(is_developer)

print(type(is_developer))

# Using the input function
my_age = input('What is your age? ')
print('My age is: ', my_age)

print(type(my_age))
