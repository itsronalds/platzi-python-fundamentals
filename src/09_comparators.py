# Greater than >

# ✅ True
print(7 > 3)

# ❌ False
print(3 > 7)

# ❌ False
print(7 > 7)

# Less than <

# ✅ True
print(5 < 6)

# ❌ False
print(6 < 5)

# ❌ False
print(5 < 5)

# Greater than or equal to >=

# ✅ True
print(2 >= 1)

# ❌ False
print(2 >= 3)

# ✅ True
print(2 >= 2)

# Less than or equal to <=

# ✅ True
print(1 <= 2)

# ❌ False
print(2 <= 1)

# ✅ True
print(2 <= 2)

# Equal to ==

# ✅ True
print(6 == 6)

# ❌ False
print(5 == 2)

# Not equal to !=

# ✅ True
print(6 != 10)

# ❌ False
print(6 != 6)

